-- ------------------------------------------------------------------
-- Задание 1. Количество клиентов без указанного штата
-- ------------------------------------------------------------------
-- Условие: посчитать клиентов, у которых поле state = NULL.
-- Используется агрегатная функция COUNT(*) и условие WHERE state IS NULL.
-- Результат: одно число — количество клиентов без штата.

SELECT COUNT(*) AS customers_without_state
FROM customers
WHERE state IS NULL;

-- ------------------------------------------------------------------
-- Задание 2. Суммарная выручка по каждой дате продажи
-- ------------------------------------------------------------------
-- Условие: для каждой даты продажи вычислить общую сумму sales_amount.
-- GROUP BY группирует записи по дате.
-- SUM(sales_amount) считает общую выручку за день.
-- ORDER BY сортирует по дате (от самой ранней к поздней).
-- Результат: таблица с двумя колонками: дата, суммарная выручка.

SELECT 
    sales_transaction_date,
    SUM(sales_amount) AS total_revenue
FROM sales
GROUP BY sales_transaction_date
ORDER BY sales_transaction_date;

-- ------------------------------------------------------------------
-- Задание 3. Типы продуктов со средней ценой от 500 до 5000
-- ------------------------------------------------------------------
-- Условие: найти типы продуктов, у которых средняя цена (base_msrp)
-- находится в диапазоне от 500 до 5000 включительно.
-- GROUP BY product_type группирует товары по типу.
-- AVG(base_msrp) вычисляет среднюю цену для каждого типа.
-- ROUND(..., 2) округляет результат до 2 знаков после запятой.
-- HAVING фильтрует группы: оставляет только те, где средняя цена от 500 до 5000.
-- ORDER BY avg_price DESC сортирует от самой дорогой средней цены к самой низкой.
-- Результат: таблица с типами продуктов и их средней ценой.

SELECT 
    product_type,
    ROUND(AVG(base_msrp), 2) AS avg_price
FROM products
GROUP BY product_type
HAVING AVG(base_msrp) BETWEEN 500 AND 5000
ORDER BY avg_price DESC;
