-- ------------------------------------------------------------------
-- 3. Проверка типов продуктов и их средней цены (без фильтрации)
-- ------------------------------------------------------------------
-- Цель: посмотреть все типы продуктов и их среднюю цену.
-- Помогает понять, какие типы попадут в задание 3 (средняя цена 500-5000),
-- а какие нет (например, automobile с ценой 50000).
-- Результат: scooter ~578, automobile ~52000 и т.д.

SELECT 
    product_type,
    ROUND(AVG(base_msrp), 2) AS avg_price
FROM products
GROUP BY product_type
ORDER BY avg_price DESC;

