-- ------------------------------------------------------------------
-- 1. Проверка: сколько уникальных сумм в канале 'dealership' и минимальной и максимальной суммы (касается 1 задания, где выводится большое количество одинаковых строк)
-- ------------------------------------------------------------------
-- Цель: показать, что данные не однотипные, просто первые строки одинаковые, а также диапозон сумм
-- Результат: количество уникальных сумм (например, 2 или больше), min_amount = 103500, max_amount = 115000 (примерно)

SELECT 
    COUNT(DISTINCT sales_amount) AS unique_amounts,
    MIN(sales_amount) AS min_amount,
    MAX(sales_amount) AS max_amount
FROM sales
WHERE channel = 'dealership';

-- =============================================
-- ОСНОВНЫЕ ЗАДАНИЯ (индивидуальный вариант 9)
-- =============================================

-- ------------------------------------------------------------------
-- Задание 1. Ранжирование продаж по сумме внутри каждого канала
-- ------------------------------------------------------------------
-- Условие: использовать RANK() для нумерации продаж от самой большой
--           к самой маленькой внутри каждого канала (channel)
-- Особенность: в данном дампе все продажи в канале 'dealership'
--              Первые 286 строк имеют сумму 115 000 (ранг 1),
--              затем идут суммы 103 500 (ранг 287 и т.д.)
-- Результат: для одинаковых сумм RANK() даёт одинаковый номер,
--            следующий ранг пропускается

SELECT 
    channel,
    sales_amount,
    RANK() OVER (PARTITION BY channel ORDER BY sales_amount DESC) AS rank_in_channel
FROM sales
ORDER BY channel, rank_in_channel;

-- ------------------------------------------------------------------
-- Задание 2. Дата следующей транзакции для каждого клиента
-- ------------------------------------------------------------------
-- Условие: для каждого клиента вывести дату текущей и следующей продажи
-- Используем LEAD(), чтобы взять значение из следующей строки
-- PARTITION BY customer_id группирует продажи по клиентам
-- ORDER BY sales_transaction_date сортирует по дате
-- Для последней продажи клиента LEAD() вернёт NULL

SELECT 
    customer_id,
    sales_transaction_date,
    LEAD(sales_transaction_date) OVER (PARTITION BY customer_id ORDER BY sales_transaction_date) AS next_transaction_date
FROM sales
ORDER BY customer_id, sales_transaction_date;

-- ------------------------------------------------------------------
-- Задание 3. Скользящее среднее продаж (по 3 транзакции) для каждого продукта
-- ------------------------------------------------------------------
-- Условие: вычислить скользящее среднее sales_amount по 3 транзакциям
--           (текущая + 2 предыдущих) для каждого продукта отдельно
-- PARTITION BY product_id группирует продажи по продуктам
-- ORDER BY sales_transaction_date задаёт порядок по дате
-- ROWS BETWEEN 2 PRECEDING AND CURRENT ROW задаёт окно:
--   текущая строка и 2 строки до неё
-- Для первых строк (менее 2 предыдущих) среднее считается по доступным

SELECT 
    product_id,
    sales_transaction_date,
    sales_amount,
    AVG(sales_amount) OVER (PARTITION BY product_id ORDER BY sales_transaction_date ROWS BETWEEN 2 PRECEDING AND CURRENT ROW) AS moving_avg_3
FROM sales
ORDER BY product_id, sales_transaction_date;
